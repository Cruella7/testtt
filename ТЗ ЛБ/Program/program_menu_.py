"""
Разработать программу с меню для демонстрации работы с типами данных:
список(list), словарь(dict), множество(set)
Меню -> выбор типа данных -> выбор метода -> краткая справка
"""
def list_s():
    print("Список(list)")
    print("1. append - добавляет элемент")
    print("2. insert - добавляет элемент на указанную позицию")
    print("3. remove - удаляет первое вхождение элемента")
    print("4. pop - удаляет и возвращает элемент по указанному индексу")
    print("5. clear - удаляет все элементы")
    print("6. index - возвращает индекс первого вхождения элемента")
    print("7. count - возвращает количество вхождений элемента")
    print("8. sort - сортирует по возрастанию")
    print("9. reverse() - изменяет порядок элементов списка на обратный")
    print("10. copy() - возвращает копию списка")

def dict_s():
    print("Словарь(dict)")
    print("1. get - возвращает значение по указанному ключу")
    print("2. keys - возвращает список всех ключей")
    print("3. values - возвращает список всех значений")
    print("4. items - возвращает список всех пар")
    print("5. clear - удаляет все ключи и значения")
    print("6. pop - удаляет и возвращает значение по указанному ключу")
    print("7. update - обновляет словарь")
    print("8. copy - возвращает копию")

def set_s():
    print("Множество(set)")
    print("1. add - добавляет элемент")
    print("2. remove - удаляет элемент")
    print("3. discard - удаляет элемент")
    print("4. pop - удаляет и возвращает произвольный элемент")
    print("5. clear - удаляет все элементы")
    print("6. union - возвращает объединение")
    print("7. difference - возвращает разность")
    print("8. copy - возвращает копию")

def main():
    while True:
        print("Меню:")
        print("1. Работа со списком")
        print("2. Работа со словарем")
        print("3. Работа с множеством")
        print("4. Выход")

        tip = input("Выберите тип данных (1-3) или введите 4 для выхода: ")
        if tip == "1":
            list_s()
        elif tip == "2":
            dict_s()
        elif tip == "3":
            set_s()
        elif tip == "4":
            break
        else:
            print("Неверный выбор. Попробуйте еще раз.")
main()
