/** М. Э. Абрамян. 1000 ЗАДАЧ ПО ПРОГРАММИРОВАНИЮ, Ростов-на-Дону 2004.
 For1. Даны целые числа K и N (N > 0). Вывести N раз число K.
 Решение: Л.Б.Гусятинер 05.09.2020 */

#include <stdio.h>

int main()
{
    int k, n;
    scanf("%d%d", &k, &n);
    for (int i = 0; i < n; ++i) {
        printf("%d%s", k, (i < n - 1 ? " " : "\n"));
    }
    getchar();

    return 0;
}
